;Clojure é uma linguagem de programação dinâmica para a
;plataforma Java, que combina programação funcional com 
;integração com o ecossistema Java. Ela não requer declaração
;de tipos e permite alterações durante a execução do programa. 
;Clojure promove a imutabilidade de dados e oferece facilidade de 
;desenvolvimento, segurança e integração com bibliotecas Java. É uma 
;escolha popular para programação funcional em ambientes concorrentes e paralelos.